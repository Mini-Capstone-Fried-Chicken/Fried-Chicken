class BuildingInfo {
  final String code;
  final String name;
  final String campus;

  final String _description; // base text only
  final List<String> floors;
  final List<String> facilities;
  final bool accessibility;
  final String department;
  final String link;

  const BuildingInfo({
    required this.code,
    required this.name,
    required this.campus,
    required String description,
    required this.floors,
    required this.facilities,
    required this.accessibility,
    required this.department,
    required this.link,
  }) : _description = description;

 
 String get description {
    final base = _description.trimRight();
    final dept = department.trim();
    if (dept.isEmpty) return base;

    final deptLower = dept.toLowerCase();
    final needsWordDepartment = !deptLower.contains('department');

    return needsWordDepartment ? "$base $dept Department." : "$base $dept";
  }

}


